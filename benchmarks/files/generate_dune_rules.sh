#!/usr/bin/env bash

set -e

shift 1
DEPS=$*

MODULES=""

function zchaff() {
    FILENAME=$1

    MODULES+=$'\n'"  zchaff_benchmark_${FILENAME}"

    cat <<EOF
(rule
 (target zchaff_benchmark_${FILENAME}.v)
 (deps ${FILENAME}.cnf ${FILENAME}.zlog)
 (action
  (write-file
   %{target}
   "From SMTCoq Require Import SMTCoq.\nSection Checker.\n  Zchaff_Checker \"${FILENAME}.cnf\" \"${FILENAME}.zlog\".\nEnd Checker.\nSection Thm.\n  Zchaff_Theorem thm \"${FILENAME}.cnf\" \"${FILENAME}.zlog\".\nEnd Thm.\nSection Parse.\n  Parse_certif_zchaff d t \"${FILENAME}.cnf\" \"${FILENAME}.zlog\".\n  Goal Sat_Checker.checker d t. vm_compute. reflexivity. Qed.\nEnd Parse.\n")))

EOF

    cat <<EOF
(rule
 (target ${FILENAME}.zlog)
 (deps run_zchaff.sh ${FILENAME}.cnf)
 (action
  (ignore-stdout
   (run ./run_zchaff.sh %{deps} %{target}))))

EOF
}


cat <<EOF
; This file has been generated by dune. Do not edit.
; Once this file is modified, track it with git.

EOF

for FILE in ${DEPS}
do

    case "${FILE}" in
        *.cnf)
            FILENAME="$(basename ${FILE%.cnf})"
            zchaff "${FILENAME}"
            ;;
    esac

done

cat <<EOF
(alias
 (name benchmark)
 (deps
  (alias_rec all)))

(rocq.theory
 (name SMTCoq.benchmarks.generated)
 (generate_project_file)
 (theories Stdlib SMTCoq)
 (plugins rocq-smtcoq.smtcoq)
 (modules${MODULES}))
EOF
